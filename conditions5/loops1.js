/* find out the difference between while and for loop */

/* while loops*/

var numValue = 0
while (numValue <= 10) {
    console.log(numValue)
    numValue++
}
console.log('\n')

/* find out the difference

/* for loop */
for (var givenNum = 0; givenNum <= 10; givenNum++) {
    console.log(givenNum)
}
console.log('\n')

/* do loop */
var someNum = 0
do {
    console.log(someNum)
    someNum++
} while (someNum <= 10) {
    console.log(someNum)
    someNum++
}